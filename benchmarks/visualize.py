"""Render charts and a Markdown report from the per-size benchmark JSON files.

This is the only module that pulls in matplotlib. It reads every
``results/bench-<size>.json`` produced by the runner and draws:

* ``scaling.png`` -- cold full build cost vs. site size;
* ``speedup.png`` -- incremental speedup over the naive rebuild per scenario;
* ``times-<size>.png`` -- naive vs. incremental median ms side by side, one per
  size.

It then writes ``results/REPORT.md`` embedding the charts and per-size tables.
matplotlib's Agg backend is selected explicitly so the charts render without a
display (CI, headless runs).
"""

from __future__ import annotations

import statistics
from pathlib import Path
from typing import Any, cast

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt

from benchmarks.metrics import load_size_report

SCENARIO_ORDER = ("rewrite_one", "edit_batch", "publish_batch", "move_batch")


def load_all(results_dir: Path) -> list[dict[str, Any]]:
    """Load every ``bench-*.json`` in ``results_dir``, sorted by document count."""
    reports = [load_size_report(p) for p in sorted(results_dir.glob("bench-*.json"))]
    return sorted(cast("list[dict[str, Any]]", reports), key=lambda r: int(r["n_docs"]))


def _scenarios(report: dict[str, Any]) -> dict[str, dict[str, Any]]:
    return {s["name"]: s for s in report["scenarios"]}


def _ordered(report: dict[str, Any]) -> list[dict[str, Any]]:
    scen = _scenarios(report)
    return [scen[name] for name in SCENARIO_ORDER if name in scen]


def chart_scaling(reports: list[dict[str, Any]], out_png: Path) -> Path:
    """Cold full build wall time vs. document count (log-log)."""
    xs = [int(r["n_docs"]) for r in reports]
    ys = [float(r["cold_incremental_ms"]) for r in reports]
    fig, ax = plt.subplots(figsize=(7, 4.5))
    ax.plot(xs, ys, marker="o")
    ax.set_xscale("log")
    ax.set_yscale("log")
    ax.set_xlabel("documents")
    ax.set_ylabel("cold full build (ms)")
    ax.set_title("Cold full build cost vs. site size")
    ax.grid(True, which="both", linestyle=":", alpha=0.5)
    fig.tight_layout()
    fig.savefig(out_png, dpi=120)
    plt.close(fig)
    return out_png


def chart_speedup(reports: list[dict[str, Any]], out_png: Path) -> Path:
    """Grouped bars: incremental speedup over naive rebuild, per scenario per size."""
    fig, ax = plt.subplots(figsize=(8, 4.5))
    width = 0.8 / max(1, len(reports))
    positions = range(len(SCENARIO_ORDER))
    for i, report in enumerate(reports):
        scen = _scenarios(report)
        heights = [float(scen[name]["speedup"]) if name in scen else 0.0 for name in SCENARIO_ORDER]
        offsets = [p + i * width for p in positions]
        ax.bar(offsets, heights, width=width, label=f"{report['size']} ({report['n_docs']})")
    ax.axhline(1.0, color="grey", linewidth=0.8, linestyle="--")
    ax.set_xticks([p + width * (len(reports) - 1) / 2 for p in positions])
    ax.set_xticklabels(SCENARIO_ORDER, rotation=15)
    ax.set_ylabel("speedup (naive ms / incremental ms)")
    ax.set_title("Incremental speedup over naive (no-cache full rebuild)")
    ax.legend()
    ax.grid(True, axis="y", linestyle=":", alpha=0.5)
    fig.tight_layout()
    fig.savefig(out_png, dpi=120)
    plt.close(fig)
    return out_png


def chart_times(report: dict[str, Any], out_png: Path) -> Path:
    """Naive vs. incremental median ms side by side for one size (log y)."""
    scen = _ordered(report)
    names = [s["name"] for s in scen]
    naive = [float(s["naive_ms"]["median"]) for s in scen]
    incr = [float(s["incremental_ms"]["median"]) for s in scen]
    positions = range(len(names))
    fig, ax = plt.subplots(figsize=(8, 4.5))
    ax.bar([p - 0.2 for p in positions], naive, width=0.4, label="naive (no cache)")
    ax.bar([p + 0.2 for p in positions], incr, width=0.4, label="incremental (cache)")
    ax.set_xticks(list(positions))
    ax.set_xticklabels(names, rotation=15)
    ax.set_yscale("log")
    ax.set_ylabel("median rebuild (ms, log)")
    ax.set_title(f"Naive vs. incremental -- {report['size']} ({report['n_docs']} docs)")
    ax.legend()
    ax.grid(True, axis="y", linestyle=":", alpha=0.5)
    fig.tight_layout()
    fig.savefig(out_png, dpi=120)
    plt.close(fig)
    return out_png


def _verdict(value: object) -> str:
    return "YES" if value is True else ("NO" if value is False else "n/a")


def write_report(reports: list[dict[str, Any]], pngs: dict[str, Path], out_md: Path) -> Path:
    """Write ``REPORT.md`` embedding the charts and per-size summary tables."""
    lines: list[str] = [
        "# pyssg incremental benchmark report",
        "",
        "Generated by `uv run python -m benchmarks`. Each size runs a cold full "
        "build, then drives four realistic docs/blog scenarios. Every scenario "
        "iteration mutates a batch of Markdown files and measures the same change "
        "two ways: **naive** (a from-scratch rebuild with the cache disabled) and "
        "**incremental** (`IncrementalSession.apply` reusing the persistent "
        "cache). The naive output doubles as the byte-identical reference; "
        "`incremental == full` must be YES.",
        "",
        "## Scaling: cold full build vs. site size",
        "",
        f"![cold build scaling]({pngs['scaling'].name})",
        "",
        "## Incremental speedup over naive",
        "",
        f"![incremental speedup]({pngs['speedup'].name})",
        "",
    ]
    for report in reports:
        size = report["size"]
        lines.append(f"## {size} -- {report['n_docs']} documents")
        lines.append("")
        lines.append(
            f"- Cold full build: **{report['cold_outputs']} outputs** in "
            f"**{float(report['cold_incremental_ms']):.0f} ms**"
        )
        lines.append("")
        if f"times-{size}" in pngs:
            lines.append(f"![naive vs incremental {size}]({pngs[f'times-{size}'].name})")
            lines.append("")
        lines.append(
            "| Scenario | Files/iter | Naive ms | Incremental ms | Speedup | "
            "Docs parsed | Cache hits | Outputs | inc == full |"
        )
        lines.append("|---|---:|---:|---:|---:|---:|---:|---:|:--:|")
        for s in _ordered(report):
            parsed = statistics.median([it["docs_parsed"] for it in s["iterations"]])
            hits = statistics.median([it["cache_hits"] for it in s["iterations"]])
            outs = statistics.median([it["outputs_changed"] for it in s["iterations"]])
            lines.append(
                f"| {s['name']} | {s['batch_size']} | "
                f"{float(s['naive_ms']['median']):.1f} | "
                f"{float(s['incremental_ms']['median']):.1f} | "
                f"{float(s['speedup']):.1f}x | "
                f"{parsed:.0f} | {hits:.0f} | {outs:.0f} | "
                f"{_verdict(s['all_identical'])} |"
            )
        lines.append("")

    out_md.write_text("\n".join(lines), encoding="utf-8")
    return out_md


def visualize(results_dir: Path) -> Path:
    """Render all charts and the Markdown report from ``results_dir``."""
    reports = load_all(results_dir)
    if not reports:
        raise FileNotFoundError(f"no bench-*.json found in {results_dir}")
    pngs = {
        "scaling": chart_scaling(reports, results_dir / "scaling.png"),
        "speedup": chart_speedup(reports, results_dir / "speedup.png"),
    }
    for report in reports:
        size = report["size"]
        pngs[f"times-{size}"] = chart_times(report, results_dir / f"times-{size}.png")
    return write_report(reports, pngs, results_dir / "REPORT.md")
